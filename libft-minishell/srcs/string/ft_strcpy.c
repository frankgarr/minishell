#include "libft.h"

int	ft_strcpy(char *dst, const char *src)
{
	if (!src)
		return (0);
	while (*src != '\0')
	{
		*dst = *src;
		dst++;
		src++;
	}
	*dst = '\0';
	return (1);
}
